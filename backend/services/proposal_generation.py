"""Real (LLM-backed) proposal draft generation, replacing
`mock_store.draft_proposal()`'s static template. See docs/architecture.md
14.5 — this is the first genuinely generative-AI feature in `backend/`.

Scope (2026-07-05, text-only v1): gathers real internal history for the
named customer via the same Provider/Evidence pipeline
`reasoning_pipeline.py` uses (`fetch_required_data` ->
`integrate_evidence` -> `interpret_evidence`), and asks Claude to draft
proposal text grounded in that history. Explicitly NOT implemented:
external trend research (web search), image sourcing/illustration — see
docs/architecture.md 14.5 for why these are deferred.

IMPORTANT (fixed 2026-07-05, same day as introduced): the first version of
this module called `LogsysProvider().fetch(...)` directly instead of
going through `fetch_required_data`/`integrate_evidence`. That bypassed
`evidence_integration.py`'s `_dedupe_records()` step, so raw
`purchase_orders` rows (which can contain many rows sharing the same
案件名/顧客名/ステータス/顧客納品日, differing only in fields not
selected — e.g. per-line-item detail) went straight to the LLM prompt
undeduplicated: a real end-to-end test surfaced ten identical
"20211203US発注分" lines in `internal_history_used`. Always route through
the same Evidence pipeline as `reasoning_pipeline.py` and `_q5_project_lookup`
— do not re-implement a second, parallel (and less correct) data-fetch
path.

Governance: proposal drafts are customer-facing content generated by an
LLM from real (but possibly incomplete) internal data, and are NOT backed
by the same kind of verifiable Provider-only Evidence pipeline
`reasoning_pipeline.py` uses for its own answers (an LLM still composes
free text from the evidence, unlike reasoning_pipeline's fixed templates)
— it could contain confident-sounding but wrong claims. Every draft is
therefore submitted to the Governance queue at `governance_level="high"`
(never auto-approved, per the Blueprint Chapter 11 Approval Levels table)
before it should be considered a finished draft a salesperson can send
out.
"""
